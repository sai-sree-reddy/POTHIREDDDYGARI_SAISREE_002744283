    private void deleteBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deleteBtnActionPerformed
        // TODO add your handling code here:
        System.out.println("here");
        //delete user from directory and table
        int deleteId = Integer.parseInt(idvalue.getText());
        boolean docFound = false;
        
        for(Doctor doctor: DoctorDirectory.arrayReturn()){
            if(doctor.id == deleteId){
                System.out.println(doctor.id + " " + deleteId);
                DoctorDirectory.arrayReturn().remove(doctor);
                docFound = true;
                break;
            }
        }
        
        if(!docFound){
            JOptionPane.showMessageDialog(this, "Doctor Not FOund", "Warning",
                    JOptionPane.ERROR_MESSAGE);
        }else{
            showTableData();
        }
        
        
    }//GEN-LAST:event_deleteBtnActionPerformed

