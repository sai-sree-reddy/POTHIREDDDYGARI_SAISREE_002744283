    public void showTableData(){
        DefaultTableModel tableModel = (DefaultTableModel) doctorDetailsTable.getModel();
        tableModel.setRowCount(0);
        ArrayList<Doctor> docDirectory = DoctorDirectory.arrayReturn();
        String loggedInAdminHospital = HospitalAdminLogin.hospitalLogeedAdminHospital;
        System.out.println("******"+loggedInAdminHospital);
        
        for (int i = 0; i < DoctorDirectory.arrayListLength(); i++) {
            System.out.println(docDirectory.get(i).hospital+"******"+loggedInAdminHospital);
            if(docDirectory.get(i).hospital.equals(loggedInAdminHospital)){
                Object[] rowData = new Object[]{
                docDirectory.get(i).id,
                docDirectory.get(i).name,
                docDirectory.get(i).email,
                docDirectory.get(i).contact,
                docDirectory.get(i).gender,
      
            };
            tableModel.addRow(rowData);
            }
        }
    }
    
    public void viewPatientsTable(){
        DefaultTableModel tableModel = (DefaultTableModel) patientDetailsTable.getModel();
        tableModel.setRowCount(0);
        String loggedInAdminHospital = HospitalAdminLogin.hospitalLogeedAdminHospital;
        for (Encounters encounter : EncounterDirectory.arrayReturn()) {
            
            if(loggedInAdminHospital.equals(encounter.Hospital)){
                Object[] rowData = new Object[]{
                encounter.patientID,
                encounter.patientName,
                encounter.patientEmail,
            };
            tableModel.addRow(rowData);
            }
        }
        
        parentPanel.removeAll();
        parentPanel.add(patientDetailPanel);
        parentPanel.repaint();
        parentPanel.revalidate();

    }
    
